  function copyItem(item) {
    var out = {};
    Object.keys(item).forEach(function (key) { out[key] = item[key]; });
    out.createdAt = Date.parse(item.matchDate + 'T12:00:00') || Date.now();
    out.editorNotes = 'تم تجهيزه من بحث نتائج كأس العالم 2026. راجع المصادر عند تحديث النتائج الجديدة.';
    return out;
  }

  function existingIds() {
    var ids = {};
    if (!window.Store) return ids;
    Store.getNews().forEach(function (item) {
      if (item && item.id) ids[item.id] = true;
    });
    return ids;
  }

  function hasAllSummaries() {
    var ids = existingIds();
    return MATCH_SUMMARIES.every(function (item) { return ids[item.id]; });
  }

  function loadSummaries(options) {
    options = options || {};
    if (!window.Store) return { ok: false, added: 0, updated: 0, skipped: 0, total: MATCH_SUMMARIES.length };

    var ids = existingIds();
    var added = 0;
    var updated = 0;
    var skipped = 0;

    MATCH_SUMMARIES.forEach(function (item) {
      if (ids[item.id] && !options.force) {
        skipped++;
        return;
      }
      var saved = Store.saveNews(copyItem(item));
      if (saved) {
        if (ids[item.id]) updated++;
        else added++;
      }
    });

    try { localStorage.setItem(FLAG_KEY, '1'); } catch (e) {}
    return { ok: true, added: added, updated: updated, skipped: skipped, total: MATCH_SUMMARIES.length };
  }

  function autoSeed() {
    if (!window.Store) return null;
    if (hasAllSummaries()) {
      try { localStorage.setItem(FLAG_KEY, '1'); } catch (e) {}
      return null;
    }
    return loadSummaries();
  }

  window.WorldCupSummaries = {
    data: MATCH_SUMMARIES,
    load: loadSummaries,
    autoSeed: autoSeed,
    hasAll: hasAllSummaries
  };
